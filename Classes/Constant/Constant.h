const static int GAME_MODE_EASY_8 = 8;
const static int GAME_MODE_NORMAL_9 = 9;
const static int GAME_MODE_HARD_10 = 10;

static int getGroupX(int index, int mode) {
	int result = 0;
	switch (mode) {
	case GAME_MODE_EASY_8:
		result = (59 * (index + 1) - 29) + index;
		break;
	case GAME_MODE_NORMAL_9:
		result = (52 * (index + 1) - 24) + index;
		break;
	case GAME_MODE_HARD_10:
		result = (46 * (index + 1) - 18) + index;
		break;
	}
	return result;
}

static int getGroupY(int index, int mode) {
	int result = 0;
	switch (mode) {
	case GAME_MODE_EASY_8:
		result = 111 * (mode - index) - index + 13;
		break;
	case GAME_MODE_NORMAL_9:
		result = 98 * (mode - index) + 19;
		break;
	case GAME_MODE_HARD_10:
		result = 88 * (mode - index) + 25;
		break;
	}
	return result;
}
